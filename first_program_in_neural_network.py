import tensorflow as tf
import numpy as np

x = np.array([[300, 15]])

layer_1 = tf.keras.layers.Dense(units=100, activation='sigmoid')
a1 = layer_1(x)

layer_2 = tf.keras.layers.Dense(units=25, activation='sigmoid')
a2 = layer_2(a1)

layer_3 = tf.keras.layers.Dense(units=1, activation='sigmoid')
a3 = layer_3(a2)

print(a3)

